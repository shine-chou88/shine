package com.gwideal.swj.certificate.entity;

public class PrivateExportEntity {
	
	private String name;
	
	private String destination;
	
	private String zjhm;
	
	private String startDate;
	
	private String endDate;
	
	private String realUseTime;
	
	private String realBackTime;
	
	private String reason;
	
	private String remark;
	
	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getZjhm() {
		return zjhm;
	}

	public void setZjhm(String zjhm) {
		this.zjhm = zjhm;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getRealUseTime() {
		return realUseTime;
	}

	public void setRealUseTime(String realUseTime) {
		this.realUseTime = realUseTime;
	}

	public String getRealBackTime() {
		return realBackTime;
	}

	public void setRealBackTime(String realBackTime) {
		this.realBackTime = realBackTime;
	}
	
	
}
